var chalk = require("chalk")


var NodeDebug = {

    /**
     * 在终端打出红色 log
     * @param text
     */
    logRed: function (text) {
        console.log( chalk.red(text))
    },

    /**
     * 在终端打出蓝色 log
     * @param text
     */
    logBlue: function (text) {
        console.log( chalk.blue(text))
    },
}

export default  NodeDebug
